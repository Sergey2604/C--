string dayWeek = Console.ReadLine();
if (dayWeek == "1") Console.WriteLine("Сегодня понедельник");
else if (dayWeek == "2") Console.WriteLine("Сегодня втроник");
else if (dayWeek == "3") Console.WriteLine("Сегодня среда");
else if (dayWeek == "4") Console.WriteLine("Сегодня четверг");
else if (dayWeek == "5") Console.WriteLine("Сегодня пятница");
else if (dayWeek == "6") Console.WriteLine("Сегодня суббота");
else if (dayWeek == "7") Console.WriteLine("Сегодня воскресенье");
else Console.Write("Ошибка ввода");